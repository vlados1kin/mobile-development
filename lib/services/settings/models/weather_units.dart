/// Класс перечислений единиц измерения температуры
enum TemperatureUnit {
  /// Градусы Цельсия
  celsius,

  /// Кельвины
  kelvin,

  /// Градусы Фаренгейта
  fahrenheit,
}

/// Класс перечислений единиц ищмерения атмосферного давления
enum PressureUnit {
  /// Паскали
  hpa,

  /// Миллиметры ртутного столба
  mmHg,
}

/// Класс перечислений единици измерения скорости ветра
enum WindSpeedUnit {
  /// Метры в секунду
  mps,

  /// Километры в час
  kph,
}
